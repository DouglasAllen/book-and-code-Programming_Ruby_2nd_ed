@arr =  [1, 2, 3, 4, 5, 6]     #=> [1, 2, 3, 4, 5, 6]
@arr.pop                       #=> 6
@arr                           #=> [1, 2, 3, 4, 5]
@arr.shift                     #=> 1
@arr                           #=> [2, 3, 4, 5]
@arr.delete_at(2)              #=> 4
@arr                           #=> [2, 3, 5]
@arr = [1, 2, 2, 3]            #=> [1, 2, 2, 3]
@arr.delete(2)                 #=> 2
@arr                           #=> [1,3]
@arr = ['foo', 0, nil, 'bar', 7, 'baz', nil] #=> ['foo', 0, nil, 'bar', 7, 'baz', nil]
@arr.compact                                 #=> ['foo', 0, 'bar', 7, 'baz']
@arr                                         #=> ['foo', 0, nil, 'bar', 7, 'baz', nil]
@arr.compact!                                #=> ['foo', 0, 'bar', 7, 'baz']
@arr                                         #=> ['foo', 0, 'bar', 7, 'baz']
@arr = [2, 5, 6, 556, 6, 6, 8, 9, 0, 123, 556] #=> [2, 5, 6, 556, 6, 6, 8, 9, 0, 123, 556]
@arr.uniq                                      #=> [2, 5, 6, 556, 8, 9, 0, 123]
@arr                                           #=> [2, 5, 6, 556, 6, 6, 8, 9, 0, 123, 556]
@arr.uniq!                                     #=> [2, 5, 6, 556, 8, 9, 0, 123]
@arr                                           #=> [2, 5, 6, 556, 8, 9, 0, 123]